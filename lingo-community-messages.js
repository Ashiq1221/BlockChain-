// Helper: get messages by type
export function getByType(type) {
  return LINGO_MESSAGES.filter(m => m.type === type);
}

// Helper: get a random subset for seeding
export function getRandomSample(n = 10) {
  const shuffled = [...LINGO_MESSAGES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, n);
}

// Helper: get messages in conversation thread order (sequential)
export function getThreads() {
  const threads = {};
  for (const msg of LINGO_MESSAGES) {
    const thread = Math.ceil(msg.id / 5);
    if (!threads[thread]) threads[thread] = [];
    threads[thread].push(msg);
  }
  return threads;
}

